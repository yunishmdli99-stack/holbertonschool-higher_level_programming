#!/usr/bin/python3
"""Calculator module with basic math functions."""


def add(a, b):
    """Return sum of a and b."""
    return a + b


def sub(a, b):
    """Return difference of a and b."""
    return a - b


def mul(a, b):
    """Return product of a and b."""
    return a * b


def div(a, b):
    """Return division of a and b."""
    return int(a / b)
